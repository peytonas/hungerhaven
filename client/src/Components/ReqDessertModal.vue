<template>
  <div id="reqDessertModal" class="modal fade" tabindex="-1" role="dialog">
    <div class="modal-dialog" role="document">
      <div class="modal-content">
        <div class="modal-header">
          <h5 class="modal-title">Request a dessert</h5>
          <button class="logout-button" data-dismiss="modal">&times;</button>
        </div>
        <div class="modal-body">
          <form>
            <div class="form-group">
              <label for="title">Desserts:</label>
              <input
                type="text"
                class="form-control"
                id="title"
                placeholder="add a dessert..."
                v-model="newDessert"
                required
              />
            </div>
            <button class="register-button" @click="reqDessert()" data-dismiss="modal">submit</button>
          </form>
        </div>
        <div class="modal-footer"></div>
      </div>
    </div>
  </div>
</template>
<script>
// import io from "socket.io-client";
export default {
  name: "reqDessertModal",
  props: [],
  data() {
    return {
      newDessert: ""
      // socket: io("localhost:3001")
    };
  },
  methods: {
    reqDessert() {
      this.event.reqDesserts.push(this.newDessert);
      this.$store.dispatch("editEvent", {
        reqDesserts: this.event.reqDesserts,
        eventId: this.event._id,
        pin: this.event.pin
      });
      // this.socket.emit("SEND_REQDESSERT", {
      //   reqDessert: this.newDessert,
      //   eventId: this.event._id,
      //   pin: this.event.pin
      // });
      this.newDessert = "";
    }
  },
  computed: {
    event() {
      return this.$store.state.event;
    }
  },
  components: {}
};
</script>
